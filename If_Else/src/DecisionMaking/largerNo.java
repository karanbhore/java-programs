package DecisionMaking;

public class largerNo {
	public static void main(String[] args) {
		int a = 10, b = 11, c = 13;

		if (a > b & a > c)
			System.out.println("a is greater:" + a);
		else if (b > a & b > c)
			System.out.println("b is greater:" + b);
		else
			System.out.println("c is greater:" + c);
	}

}
