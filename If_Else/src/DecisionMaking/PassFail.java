package DecisionMaking;

public class PassFail {
	public static void main(String[] args) {
		int m1 = 20, m2 = 50;

		if (m1 > 40)
			System.out.println("m1 is pass" + "  " + m1);
		else
			System.out.println("m1 is fail" + "  " + m1);
		if (m2 > 40)
			System.out.println("m2 is pass" + "  " + m2);
		else
			System.out.println("m2 is fail" + "  " + m2);
	}

}
